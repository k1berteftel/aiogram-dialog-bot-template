

def get_age_text(age: int) -> str:
    """
        Возвращает слово "лет" или "год"| "года" в зависимости от возраста
    """
    if age in [1, 21, 31, 41, 51, 61, 71, 81, 91, 101]:
        return 'год'
    elif age in [2, 3, 4, 22, 23, 24, 32, 33, 34, 42, 43, 44, 52, 53, 54,
                 62, 63, 64, 72, 73, 74, 82, 83, 84, 92, 93, 94, 102, 103, 104, 112, 113, 114]:
        return 'года'
    else:
        return 'лет'
